    public void Actualizar(){
       
        Statement b;
            posicion_cosecha = tx_cosecha.getSelectedIndex();
            Id_orden_trilla = new ArrayList<>();
            ot_codigo.removeAllItems();
            ot_cafe_utilizado.setText("");
            ot_cliente.setText("");
            ot_fecha.setText("");
            ot_marcas.setText("");
            ot_preparacion.setText("");
            
            try {
                b = base.createStatement();
                ResultSet consultab = b.executeQuery("SELECT ordentrilla.Codigo FROM ordentrilla WHERE ordentrilla.Disponible = 1 && ordentrilla.Cosecha_Id = "+Id_cosecha.get(posicion_cosecha)+";");
                while(consultab.next()){
                    String Id = consultab.getString(1);
                    Id_orden_trilla.add(Id);
                    ot_codigo.addItem(Id);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Nota_Rendimiento.class.getName()).log(Level.SEVERE, null, ex);
            }
    }

