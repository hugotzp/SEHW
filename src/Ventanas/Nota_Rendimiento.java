    private void ot_codigoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ot_codigoItemStateChanged
        if(evt.getStateChange() == ItemEvent.SELECTED){
            Statement b;
            posicion_orden_trilla = ot_codigo.getSelectedIndex();
            try {
                b = base.createStatement();
                ResultSet consultab = b.executeQuery("SELECT ordentrilla.Fecha,ordentrilla.Cliente,ordentrilla.Preparacion,ordentrilla.Marca,ordentrilla.CafeUsado FROM ordentrilla WHERE ordentrilla.Codigo = "+Id_orden_trilla.get(posicion_orden_trilla)+";");
                if(consultab.next()){
                    ot_fecha.setText(consultab.getString(1));
                    ot_cliente.setText(consultab.getString(2));
                    ot_preparacion.setText(consultab.getString(3));
                    ot_marcas.setText(consultab.getString(4));
                    ot_cafe_utilizado.setText(consultab.getString(5));
                }
            } catch (SQLException ex) {
                Logger.getLogger(Nota_Rendimiento.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_ot_codigoItemStateChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(posicion_orden_trilla!= -1){
            String codigo = (String)ot_codigo.getSelectedItem();
            if(!Ordenes_agregadas.contains(codigo)){
                DefaultTableModel tabla = (DefaultTableModel)Ordenes_Trilla.getModel();
                String[] agregar = new String[4];
                agregar[0] = codigo;
                agregar[1] = ot_preparacion.getText();
                agregar[2] = ot_marcas.getText();
                agregar[3] = ot_cafe_utilizado.getText();
                tabla.addRow(agregar);
                Ordenes_agregadas.add(codigo);
            }else{
                JOptionPane.showMessageDialog(this, "La orden ya ha sido agregada", "ERROR", JOptionPane.ERROR_MESSAGE, null);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

